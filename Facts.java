package suncoastChapter2;

public class Facts {

	public static void main(String[] args) {
		// String concatenation
		System.out.println("We present the following facts for your "
				+ "extracurricular edifications");	
		
		// Prints a blank line
		System.out.println();
		
		// String containing numeric digits
		System.out.println("Letters in the Hawaiian alphabet: 12");
		
		// Concatenate numeric value to a string
		System.out.println("Year in which da Vinci invented the parachute: " + 1515);
		
		
		
		
	}
}
