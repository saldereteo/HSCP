package Patterns;

/**
 * Demonstrates how to walk through an array from first element to last
 * @author 1116444
 *
 */
public class WalkThroughArray {

	public static void main(String[] args) {
		
		int[] arr = {1, 2, 3, 4, 5};
		
		for(int i = 0; i < arr.length; i++) {
			System.out.println(arr[i]);
		}
		
	}
}
